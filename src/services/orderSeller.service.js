import { ErrorResponse } from '../utils/errorResponse.js';

/**
 * Create a new order
 */
export const createOrder = async (orderData) => {
  const {
    userId,
    subtotal,
    shippingCost = 0,
    tax = 0,
    discount = 0,
    totalPrice,
    shippingAddress,
    shippingMethod = 'standard',
    paymentMethod = 'cash_on_delivery',
    discountCode,
    notes,
    items = [],
  } = orderData;

  // Validation
  if (!userId || !subtotal || !totalPrice || !shippingAddress || !paymentMethod) {
    throw new ErrorResponse(
      'Missing required fields: userId, subtotal, totalPrice, shippingAddress, paymentMethod',
      400
    );
  }

  // Validate userId is a valid ObjectId
  if (!mongoose.Types.ObjectId.isValid(userId)) {
    throw new ErrorResponse(
      `Invalid userId format. Must be a valid MongoDB ObjectId. Received: ${userId}`,
      400
    );
  }

  // Create order - orderNumber will be auto-generated by pre-save hook
  const order = new Order({
    userId,
    subtotal,
    shippingCost,
    tax,
    discount,
    totalPrice,
    shippingAddress,
    shippingMethod,
    paymentMethod,
    discountCode,
    notes,
    status: 'pending',
    paymentStatus: 'pending',
  });

  await order.save();

  // Create order items if provided
  if (items && items.length > 0) {
    const orderItems = await Promise.all(
      items.map((item) =>
        OrderItem.create({
          orderId: order._id,
          productId: item.productId,
          modelId: item.modelId,
          sku: item.sku,
          quantity: item.quantity,
          price: item.price,
          tierSelections: item.tierSelections || {},
          subtotal: item.subtotal || item.quantity * item.price,
          originalPrice: item.originalPrice,
          isFlashSale: item.isFlashSale || false,
        })
      )
    );

    order.items = orderItems.map((item) => item._id);
    await order.save();
  }

  // Populate and return
  const populatedOrder = await Order.findById(order._id).populate('items');

  return populatedOrder;
};

/**
 * Get all orders with pagination and filters
 * Returns orders that contain products from the seller
 */
export const getSellerOrders = async (filters = {}, sellerId) => {
  const { page = 1, limit = 10, status, sortBy = 'createdAt' } = filters;
  const skip = (page - 1) * limit;

  if (!sellerId) {
    throw new ErrorResponse('Seller ID is required', 400);
  }

  // Find all products belonging to this seller
  const sellerProducts = await Product.find({ sellerId }).select('_id');
  const sellerProductIds = sellerProducts.map(p => p._id);

  if (sellerProductIds.length === 0) {
    return {
      total: 0,
      page: Number(page),
      limit: Number(limit),
      pages: 0,
      data: [],
    };
  }

  // Find OrderItems containing seller's products
  const orderItems = await OrderItem.find({ productId: { $in: sellerProductIds } }).select('orderId');
  const orderIds = [...new Set(orderItems.map(item => item.orderId.toString()))].map(
    id => new mongoose.Types.ObjectId(id)
  );

  if (orderIds.length === 0) {
    return {
      total: 0,
      page: Number(page),
      limit: Number(limit),
      pages: 0,
      data: [],
    };
  }

  // Build filter
  const filterQuery = { _id: { $in: orderIds } };
  if (status) {
    filterQuery.status = status;
  }

  // Sort options
  const sortOptions = {
    createdAt: { createdAt: -1 },
    'newest-first': { createdAt: -1 },
    'oldest-first': { createdAt: 1 },
    'total-high': { totalPrice: -1 },
    'total-low': { totalPrice: 1 },
  };

  const orders = await Order.find(filterQuery)
    .populate('userId', 'fullName email phone')
    .populate({
      path: 'items',
      populate: {
        path: 'productId',
        select: 'name images originalPrice models tiers',
      },
    })
    .sort(sortOptions[sortBy] || { createdAt: -1 })
    .skip(skip)
    .limit(Number(limit));

  const total = await Order.countDocuments(filterQuery);

  // Enrich items with selected model only
  const enrichedOrders = orders.map(order => ({
    ...order.toObject(),
    items: order.items.map(item => {
      const selectedModel = item.productId.models?.find(m => m._id.toString() === item.modelId.toString());
      const tierDetails = enrichTierDetails(item, selectedModel, item.productId.tiers);

      return {
        ...item.toObject(),
        tierDetails,
        productId: {
          ...item.productId.toObject(),
          selectedModel,
          models: undefined,
          tiers: undefined,
        },
      };
    }),
  }));

  return {
    total,
    page: Number(page),
    limit: Number(limit),
    pages: Math.ceil(total / limit),
    data: enrichedOrders,
  };
};

/**
 * Get orders filtered by specific status
 */
export const getOrdersByStatus = async (status, pagination = {}) => {
  const { page = 1, limit = 10 } = pagination;
  const skip = (page - 1) * limit;

  const validStatuses = [
    'pending',
    'processing',
    'shipped',
    'delivered',
    'delivered_pending_confirmation',
    'completed',
    'cancelled',
    'refunded',
    'refund_pending',
    'under_investigation',
  ];

  if (!validStatuses.includes(status)) {
    throw new ErrorResponse(
      `Invalid status. Valid statuses: ${validStatuses.join(', ')}`,
      400
    );
  }

  const filter = { status };

  const orders = await Order.find(filter)
    .populate('userId', 'fullName email phone')
    .populate({
      path: 'items',
      populate: {
        path: 'productId',
        select: 'name images originalPrice models tiers',
      },
    })
    .sort({ createdAt: -1 })
    .skip(skip)
    .limit(Number(limit));

  const total = await Order.countDocuments(filter);

  const statusCounts = await Order.aggregate([
    { $group: { _id: '$status', count: { $sum: 1 } } },
  ]);

  // Enrich items with selected model only
  const enrichedOrders = orders.map(order => ({
    ...order.toObject(),
    items: order.items.map(item => {
      const selectedModel = item.productId.models?.find(m => m._id.toString() === item.modelId.toString());
      const tierDetails = enrichTierDetails(item, selectedModel, item.productId.tiers);

      return {
        ...item.toObject(),
        tierDetails,
        productId: {
          ...item.productId.toObject(),
          selectedModel,
          models: undefined,
          tiers: undefined,
        },
      };
    }),
  }));

  return {
    total,
    page: Number(page),
    limit: Number(limit),
    pages: Math.ceil(total / limit),
    statusCounts: statusCounts.reduce((acc, item) => {
      acc[item._id] = item.count;
      return acc;
    }, {}),
    data: enrichedOrders,
  };
};

/**
 * Get order status history (audit log)
 */
export const getOrderStatusHistory = async (orderId) => {
  const order = await Order.findById(orderId)
    .select('orderNumber status statusHistory')
    .populate('statusHistory.changedBy', 'fullName email role');

  if (!order) {
    throw new ErrorResponse('Order not found', 404);
  }

  return {
    orderNumber: order.orderNumber,
    currentStatus: order.status,
    history: order.statusHistory.sort((a, b) => new Date(b.changedAt) - new Date(a.changedAt)),
  };
};

