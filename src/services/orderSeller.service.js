/**
 * Update order status with validation
 */
export const updateOrderStatus = async (orderId, updateData, userData) => {
  const { newStatus, trackingNumber, estimatedDelivery, notes, reason } = updateData;
  const { userId, userRole } = userData;

  const order = await Order.findById(orderId);

  if (!order) {
    throw new ErrorResponse('Order not found', 404);
  }

  // Validate status transition
  const validTransitions = {
    pending: ['processing', 'cancelled'],
    processing: ['shipped', 'cancelled'],
    shipped: ['delivered', 'refund_pending'],
    delivered: ['delivered_pending_confirmation'],
    delivered_pending_confirmation: ['completed', 'refund_pending'],
    completed: [],
    cancelled: [],
    refund_pending: ['refunded', 'under_investigation'],
    refunded: [],
    under_investigation: ['completed', 'refunded'],
  };

  if (!validTransitions[order.status]?.includes(newStatus)) {
    throw new ErrorResponse(
      `Cannot transition from '${order.status}' to '${newStatus}'`,
      400
    );
  }

  // Store old status for history
  const oldStatus = order.status;

  // Update status
  order.status = newStatus;

  // Update tracking and estimated delivery if provided
  if (trackingNumber) order.trackingNumber = trackingNumber;
  if (estimatedDelivery) order.estimatedDelivery = estimatedDelivery;
  if (notes) order.notes = notes;

  // Add to status history
  order.statusHistory.push({
    status: newStatus,
    changedBy: userId,
    changedByRole: userRole,
    changedAt: new Date(),
    reason: reason || `Changed from ${oldStatus} to ${newStatus}`,
    notes: notes || undefined,
  });

  await order.save();

  const updatedOrder = await Order.findById(orderId)
    .populate('userId', 'fullName email phone')
    .populate({
      path: 'items',
      populate: {
        path: 'productId',
        select: 'name images originalPrice models tiers',
      },
    })
    .populate('statusHistory.changedBy', 'fullName email role');

  // Enrich items with selected model only
  const enrichedOrder = {
    ...updatedOrder.toObject(),
    items: updatedOrder.items.map(item => {
      const selectedModel = item.productId.models?.find(m => m._id.toString() === item.modelId.toString());
      const tierDetails = enrichTierDetails(item, selectedModel, item.productId.tiers);

      return {
        ...item.toObject(),
        tierDetails,
        productId: {
          ...item.productId.toObject(),
          selectedModel,
          models: undefined,
          tiers: undefined,
        },
      };
    }),
  };

  return enrichedOrder;
};

/**
 * Cancel order with reason
 */
export const cancelOrder = async (orderId, cancellationReason) => {
  const order = await Order.findById(orderId);

  if (!order) {
    throw new ErrorResponse('Order not found', 404);
  }

  // Can only cancel pending or processing orders
  if (!['pending', 'processing'].includes(order.status)) {
    throw new ErrorResponse(
      `Cannot cancel order with status '${order.status}'`,
      400
    );
  }

  order.status = 'cancelled';
  order.cancelledAt = new Date();
  order.cancellationReason = cancellationReason || 'Cancelled';

  await order.save();

  const updatedOrder = await Order.findById(orderId)
    .populate('userId', 'fullName email phone')
    .populate({
      path: 'items',
      populate: {
        path: 'productId',
        select: 'name images originalPrice models tiers',
      },
    });

  // Enrich items with selected model only
  const enrichedOrder = {
    ...updatedOrder.toObject(),
    items: updatedOrder.items.map(item => {
      const selectedModel = item.productId.models?.find(m => m._id.toString() === item.modelId.toString());
      const tierDetails = enrichTierDetails(item, selectedModel, item.productId.tiers);

      return {
        ...item.toObject(),
        tierDetails,
        productId: {
          ...item.productId.toObject(),
          selectedModel,
          models: undefined,
          tiers: undefined,
        },
      };
    }),
  };

  return enrichedOrder;
};

